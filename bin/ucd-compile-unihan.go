package main

import (
	_ "archive/zip"
	_ "flag"
	"fmt"
	"log"
	"net/http"
	_ "strings"
	"time"
)

func main() {

	// flag.Parse()

	unihan_data := "http://unicode.org/Public/UCD/latest/ucd/Unihan.zip"

	// extract: Unihan_Readings.txt
	// look for: U+F9B7	kDefinition	sweet wine; sweet spring

	rsp, err := http.Get(unihan_data)
	defer rsp.Body.Close()

	if err != nil {
		log.Fatal(err)
	}

	ts := time.Now()

	fmt.Printf("%s\n\n", "package unihan")

	fmt.Printf("/* %s */\n", unihan_data)
	fmt.Printf("/* This file was generated by robots at %s */\n\n", ts.UTC())

	fmt.Printf("%s\n", "var UCDHan = map[string]string{")

	fmt.Printf("%s\n", "}")
}
